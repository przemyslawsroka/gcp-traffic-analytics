/*
 * Convert string representation of IPv4 address to INT64
 */
CREATE TEMP FUNCTION ip_to_int(ip string) AS (
 CAST((CAST(REGEXP_SUBSTR(ip, '[0-9]+', 1, 1) as INT64) * power(2, 24) +
 CAST(REGEXP_SUBSTR(ip, '[0-9]+', 1, 2) as INT64) * power(2, 16) +
 CAST(REGEXP_SUBSTR(ip, '[0-9]+', 1, 3) as INT64) * power(2, 8) +
 CAST(REGEXP_SUBSTR(ip, '[0-9]+', 1, 4) as INT64)) AS INT64)
);

/*
 * Return string representation of protocol.
 */
CREATE TEMP FUNCTION PORTS_TO_PROTO(protocol INT64, src_port INT64, dst_port INT64) AS (
    CASE
        WHEN protocol = 1 THEN 'icmp'
        WHEN src_port = 22
        OR dst_port = 22 THEN 'ssh'
        WHEN src_port = 80
        OR dst_port = 80 THEN 'http'
        WHEN src_port = 443
        OR dst_port = 443 THEN 'https'
        WHEN src_port = 10402
        OR dst_port = 10402 THEN 'gae' -- App Engine flexible environment
        WHEN src_port = 8443
        OR dst_port = 8443 THEN 'gae' -- App Engine flexible environment
        ELSE FORMAT('other-%d->%d', src_port, dst_port)
    END
);

/*
 * Return information about external ip addresses that are exchanging data (INGRESS or EGRESS) with current project based on VPC Flow Logs.
 */
SELECT
 local_ip,
 vpc_name,
 vm_name,
 external_ip,
 protocol,
 IP_TO_TYPE(ip_to_int(external_ip)) as external_type,
 traffic_type,
 total_bytes_sent
FROM
(
SELECT
  JSON_VALUE(json_payload.connection.src_ip) as local_ip,
  JSON_VALUE(json_payload.src_vpc.vpc_name) as vpc_name,
  JSON_VALUE(json_payload.src_instance.vm_name) as vm_name,
  JSON_VALUE(json_payload.connection.dest_ip) as external_ip,
  PORTS_TO_PROTO(
          CAST(
              JSON_VALUE(json_payload.connection.protocol) AS INT64
          ),
          CAST(
              JSON_VALUE(json_payload.connection.src_port) AS INT64
          ),
          CAST(
              JSON_VALUE(json_payload.connection.dest_port) AS INT64
          )
      ) AS protocol,
  "EGRESS" as traffic_type,
  SUM(CAST(JSON_VALUE(json_payload.bytes_sent) as INT64)) as total_bytes_sent
FROM
  `logs__Default_US._AllLogs`
WHERE
  log_name LIKE "%compute.googleapis.com%2Fvpc_flows%"
  AND timestamp(replace(substr(JSON_VALUE(json_payload.start_time),0,19),"T"," ")) > TIMESTAMP_SUB(CURRENT_TIMESTAMP(), INTERVAL 1 HOUR)
  AND (JSON_VALUE(json_payload.src_vpc.vpc_name) is null or JSON_VALUE(json_payload.dest_vpc.vpc_name) is null)
  AND JSON_VALUE(json_payload.reporter) = "SRC"
GROUP BY 1,2,3,4,5,6
UNION ALL
SELECT
  JSON_VALUE(json_payload.connection.dest_ip) as local_ip,
  JSON_VALUE(json_payload.dest_vpc.vpc_name) as vpc_name,
  JSON_VALUE(json_payload.dest_instance.vm_name) as vm_name,
  JSON_VALUE(json_payload.connection.src_ip) as external_ip,
  PORTS_TO_PROTO(
          CAST(
              JSON_VALUE(json_payload.connection.protocol) AS INT64
          ),
          CAST(
              JSON_VALUE(json_payload.connection.src_port) AS INT64
          ),
          CAST(
              JSON_VALUE(json_payload.connection.dest_port) AS INT64
          )
      ) AS protocol,
  "INGRESS" as traffic_type,
  SUM(CAST(JSON_VALUE(json_payload.bytes_sent) as INT64)) as total_bytes_sent
FROM
  `logs__Default_US._AllLogs`
WHERE
  log_name LIKE "%compute.googleapis.com%2Fvpc_flows%"
  AND timestamp(replace(substr(JSON_VALUE(json_payload.start_time),0,19),"T"," ")) > TIMESTAMP_SUB(CURRENT_TIMESTAMP(), INTERVAL 1 HOUR)
  AND (JSON_VALUE(json_payload.src_vpc.vpc_name) is null or JSON_VALUE(json_payload.dest_vpc.vpc_name) is null)
  AND JSON_VALUE(json_payload.reporter) = "DEST"
GROUP BY 1,2,3,4,5,6
)
ORDER BY 8 DESC
